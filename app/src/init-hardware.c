unsigned char reverseByte(unsigned char b) {

  b = (b & 0xF0) >> 4 | (b & 0x0F) << 4;
  b = (b & 0xCC) >> 2 | (b & 0x33) << 2;
  b = (b & 0xAA) >> 1 | (b & 0x55) << 1;

  return b;
}

int writeCommandLCD(unsigned char command) {

  if (command_fd < 0) {
    return ERROR;
  }

  return write(command_fd, &command, 1);
}

int writeDataLCD(unsigned char *buff, int size) {

	if (data_fd < 0) {
		return ERROR;
	}

	return write(data_fd, buff, size);
}

int writeTemplateLCD() {

	int i, j;
	unsigned char buff1[BUFFER_LENGHT], *pchBuff;
	unsigned char command[NUM_CMDS] = { PAGE_ADDR_0, COL_ADDR_0, BOOSTER_6X };
	unsigned char (*pchBufferLang)[BUFFER_LENGHT];

  if (!g_bSupportDisplay) {
    return SUCCESS;
  }

  if (g_numLinesDisplay == NUM_LINES_128x32) {

    switch (g_sysLanguage) {
      case LANG_PORTUGUESE:
        pchBufferLang = bufferLCDPortuguese_128x32;
        break;
      case LANG_SPANISH:
        pchBufferLang = bufferLCDSpanish_128x32;
        break;
      case LANG_ENGLISH:
        pchBufferLang = bufferLCDEnglish_128x32;
        break;
      default:
        pchBufferLang = bufferLCDPortuguese_128x32;
        break;
    }

  } else if (g_numLinesDisplay == NUM_LINES_128x64) {
      switch (g_sysLanguage) {

        case LANG_PORTUGUESE:
          pchBufferLang = bufferLCDPortuguese_128x64;
          break;
        case LANG_SPANISH:
          pchBufferLang = bufferLCDSpanish_128x64;
          break;
        case LANG_ENGLISH:
          pchBufferLang = bufferLCDEnglish_128x64;
          break;
        default:
          pchBufferLang = bufferLCDPortuguese_128x64;
          break;
      }
  }

	writeCommandLCD(START_LINE_0);

	for (i = 0; i < g_numLinesDisplay; i++) {
		pchBuff = &pchBufferLang[i][0];
		for (j = 0; j < BUFFER_LENGHT; j++) {
			pchBufferLang[i][j] = reverseByte(pchBufferLang[i][j]);
		}
	}

	// Screen print inverted
	for (i = (g_numLinesDisplay - 1); i >= 0; i--) {
		writeCommandLCD(command[0]);
    writeCommandLCD(command[1]);
    writeCommandLCD(command[2]);
		pchBuff = &pchBufferLang[i][0];
		for (j = (BUFFER_LENGHT - 1); j >= 0; j--) {
			buff1[(BUFFER_LENGHT - 1) - j] = pchBufferLang[i][j];
		}
		writeDataLCD(buff1, BUFFER_LENGHT);
		command[0]++;
	}

	return SUCCESS;
}

void turnOnBacklight() {

	if (g_bSupportBacklight) {
		write(backlight_fd, backLighOn, 2);
	}
}

int led_write_data(unsigned char *data, unsigned char size) {

	int retval;

	if (size % 2) {
		return EIO;
	}

	retval = write(led_fd, data, size);

	if (retval == 2) {
		return 0;
	}	else {
		return errno;
	}
}

int ledOnOff(bool bOn, unsigned char ledType) {
	unsigned char data[2] = { ledType, 1 };

	data[1] = bOn;

	return led_write_data(data, 2);
}

void *blinkLeds() {

	while (!bEndApp) {
		ledOnOff(true, g_ledID);
		usleep(SLEEP_BEFORE_BLINK);
    ledOnOff(false, g_ledID);
    usleep(SLEEP_BEFORE_BLINK);
	}

  ledOnOff(false, g_ledID);

	return NULL;
}

void ctrlLeds() {

	pthread_t procThrId;
	int error;

  error = (int) pthread_create(&procThrId, NULL, blinkLeds, NULL);
  if (error) {
  	log_error("Fail Leds Thread");
  }
}

void ctrlBacklight() {
	turnOnBacklight();
	writeTemplateLCD();	
}

int open_data_conection(void) {

	data_fd = open(DEV_LCD_DATA, O_RDWR);

	return data_fd;
}

int open_command_conection(void) {

	command_fd = open(DEV_LCD_CMD, O_RDWR);

	return command_fd;
}

int openDriverLed() {

  led_fd = open(DEV_LEDS, O_RDWR);

	return led_fd;
}

int open_backlight(void) {

	backlight_fd = open(DEV_BACKLIGHT, O_RDWR);

	return backlight_fd;
}

int openDrivers() {

	if (openDriverLed() < 0) {
		return ERROR_LED;
	}

  if (g_bSupportDisplay) {
    if (open_command_conection() < 0) {
      return ERROR_LCD_CMD;
    }

    if (open_data_conection() < 0) {
      return ERROR_LCD_DATA;
    }  
  }

  if (g_bSupportBacklight) {
    if (open_backlight() < 0) {
      return ERROR_BACKLIGHT;
    }
  }

	return SUCCESS;
}

int getLanguage() {

	int language;
	char *pchLanguage   = NULL;
  struct _db_connection *connDB;
  struct _db_result result;
	
  connDB = db_get_connection("/data/database.sql");
  if (!connDB) {
    log_error("Database unreacheable");
    return LANG_PORTUGUESE;
  }

  if (db_query_select(connDB, "SELECT SYSPhoneLanguage FROM TAB_SYSTEM_PHONE", &result) == DATABASE_OK) {
    pchLanguage  = o_strdup(((struct _db_type_text *)result.data[0][0].t_data)->value);
  } else {
    log_error("Database read error");
    return LANG_PORTUGUESE;
  }

	if (pchLanguage != NULL) {
		if (!o_strcmp(pchLanguage, PORTUGUESE)) {
			language = LANG_PORTUGUESE;
		} else if (!o_strcmp(pchLanguage, SPANISH)) {
			language = LANG_SPANISH;
		} else if (!o_strcmp(pchLanguage, ENGLISH)) {
			language = LANG_ENGLISH;
		} else {
			language = LANG_PORTUGUESE;
		}
	} else {
		language = LANG_PORTUGUESE;
	}

	db_close_connection(connDB);

	return language;
}

void getConfig() {

  g_sysLanguage = getLanguage();

	if (iniparser_open()) {
		log_error("Cannot parse file");
		return;
	}
  
  iniparser_get_config(CFG_LED_ID,            &g_ledID,             TYPE_INT);
  iniparser_get_config(CFG_BACKLIGHT_SUPPORT, &g_bSupportBacklight, TYPE_BOOL);
  iniparser_get_config(CFG_DISPLAY_SUPPORT,   &g_bSupportDisplay,   TYPE_BOOL);
  if (g_bSupportDisplay) {

    char *pchDisplaySize = NULL;  
    iniparser_get_config(CFG_DISPLAY_SIZE,   &pchDisplaySize,   TYPE_BOOL);
    if (pchDisplaySize) {
      char *pchToken;

      pchToken = strtok(pchDisplaySize, ",");
      if (pchToken) {
        g_widthDisplay = atoi(pchToken);
        pchToken = strtok(NULL, ",");
        if (pchToken) {
          g_heightDisplay = atoi(pchToken);
        } else {
          g_heightDisplay = MAX_HEIGHT; 
        }
      } else {
        log_error("Invalid display size"); 
        g_heightDisplay = MAX_HEIGHT;
      }

      g_numLinesDisplay = g_heightDisplay / SIZE_WORD;
    }    
  }  

  iniparser_close();
}

static int initSystem() {

	int status;

	getConfig();

	status = openDrivers();
	if (status != SUCCESS) {
		log_error("Error to open a driver %d", status);
		return ERROR;
	}

  return status;
}

int main(void) {

	if (initSystem() != SUCCESS) {
		return EXIT_FAILURE;
	}

  ctrlLeds();
	ctrlBacklight();																									

  // sleep to blink leds and update LCD
	sleep(SLEEP_BEFORE_CLOSE);	
  bEndApp = true;
  usleep(SLEEP_BEFORE_BLINK*2);

	return EXIT_SUCCESS;
}
